package capgemini;

public class TiposDado {

	public static void main(String[] args) {
		// demonstração de alguns tipos de dados
		int numero;
		float decimalCurto;
		double decimalLongo;
		char caracter;
		String cadeiaCaracteres;
		
		numero = 10;
		decimalCurto = 10.5f;
		decimalLongo = 20.5f;
		caracter = 'A';
		cadeiaCaracteres = "Curso Java Fundamentos CapGemini";
		
		
		System.out.println(numero);
		System.out.println(decimalCurto);
		System.out.println(decimalLongo);
		System.out.println(caracter);
		System.out.println(cadeiaCaracteres);
		
	}

}
